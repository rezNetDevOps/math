// Package math is my first Golang package for learning purpose to publish package on github
// Four functions for do basic math oprations Add Sub Div Mul
package math

func Add(a, b int) int {
	return a + b
}

func Sub(a, b int) int {
	return a - b
}

func Div(a, b int) int {
	return a / b
}

func Mul(a, b int) int {
	return a * b
}
